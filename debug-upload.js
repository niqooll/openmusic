const axios = require('axios');
const path = require('path');

async function testUpload() {
  try {
    // Create a simple test image file
    const testImagePath = path.join(__dirname, 'test-image.jpg');
    
    // Create minimal JPEG file (1x1 pixel)
    const minimalJpeg = Buffer.from([
      0xFF, 0xD8, 0xFF, 0xE0, 0x00, 0x10, 0x4A, 0x46, 0x49, 0x46, 0x00, 0x01,
      0x01, 0x01, 0x00, 0x48, 0x00, 0x48, 0x00, 0x00, 0xFF, 0xDB, 0x00, 0x43,
      0x00, 0x08, 0x06, 0x06, 0x07, 0x06, 0x05, 0x08, 0x07, 0x07, 0x07, 0x09,
      0x09, 0x08, 0x0A, 0x0C, 0x14, 0x0D, 0x0C, 0x0B, 0x0B, 0x0C, 0x19, 0x12,
      0x13, 0x0F, 0x14, 0x1D, 0x1A, 0x1F, 0x1E, 0x1D, 0x1A, 0x1C, 0x1C, 0x20,
      0x24, 0x2E, 0x27, 0x20, 0x22, 0x2C, 0x23, 0x1C, 0x1C, 0x28, 0x37, 0x29,
      0x2C, 0x30, 0x31, 0x34, 0x34, 0x34, 0x1F, 0x27, 0x39, 0x3D, 0x38, 0x32,
      0x3C, 0x2E, 0x33, 0x34, 0x32, 0xFF, 0xC0, 0x00, 0x11, 0x08, 0x00, 0x01,
      0x00, 0x01, 0x01, 0x01, 0x11, 0x00, 0x02, 0x11, 0x01, 0x03, 0x11, 0x01,
      0xFF, 0xC4, 0x00, 0x14, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x08, 0xFF, 0xC4,
      0x00, 0x14, 0x10, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xFF, 0xDA, 0x00, 0x0C,
      0x03, 0x01, 0x00, 0x02, 0x11, 0x03, 0x11, 0x00, 0x3F, 0x00, 0xB2, 0xC0,
      0x07, 0xFF, 0xD9
    ]);

    fs.writeFileSync(testImagePath, minimalJpeg);
    console.log('Test image created');

    // First create an album
    const albumResponse = await axios.post('http://localhost:5000/albums', {
      name: 'Test Album',
      year: 2024
    });

    const albumId = albumResponse.data.data.albumId;
    console.log('Album created:', albumId);

    // Now try to upload cover
    const form = new FormData();
    form.append('cover', fs.createReadStream(testImagePath), {
      filename: 'test.jpg',
      contentType: 'image/jpeg'
    });

    const uploadResponse = await axios.post(
      `http://localhost:5000/albums/${albumId}/covers`,
      form,
      {
        headers: {
          ...form.getHeaders(),
        },
      }
    );

    console.log('Upload successful:', uploadResponse.data);

    // Clean up test file
    fs.unlinkSync(testImagePath);

  } catch (error) {
    console.error('Error details:', {
      message: error.message,
      status: error.response?.status,
      statusText: error.response?.statusText,
      data: error.response?.data
    });
  }
}

// Run if called directly
if (require.main === module) {
  testUpload();
}

module.exports = testUpload;